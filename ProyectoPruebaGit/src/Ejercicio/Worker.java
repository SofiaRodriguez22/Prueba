package Ejercicio;

public class Worker {
	String name;
	int age;
	String job;
	
	public Worker(String name, int age, String job) {
		this.name = name;
		this.age = age;
		this.job = job;
	}
	
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public String getJob() {
		return job;
	}
}
